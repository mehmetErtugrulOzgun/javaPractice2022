package p17_forLoop;

import java.util.Scanner;

public class C03_ForLoop {
    public static void main(String[] args) {
        // 100den baslayarak 50ye kadar olan sayilari aralarinde virgul olarak ayni satirda yazdirin

        for (int i=100;i>=51;i--){
            System.out.print(i+", ");
        }
        System.out.print("50");
        }
    }