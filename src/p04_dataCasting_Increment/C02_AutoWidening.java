package p04_dataCasting_Increment;

public class C02_AutoWidening {
    public static void main(String[] args) {

        byte sayi1 = 31;
        System.out.println(sayi1);

        short sayi2 = sayi1;
        System.out.println(sayi2);

        double sayi3 = sayi1;
        System.out.println(sayi3);

    }
}
